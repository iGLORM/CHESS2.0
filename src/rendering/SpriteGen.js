  static generatePieceSprite(type, color, themeColors, size) {
    const canvas = document.createElement('canvas');
    canvas.width = size;
    canvas.height = size;
    const ctx = canvas.getContext('2d');
    ctx.imageSmoothingEnabled = false;

    const pixels = this.PIECE_PIXELS[type] || this.PIECE_PIXELS.pawn;
    const pixelSize = Math.ceil(size / 32);

    // Mario World style colors
    const isWhite = color === 'white';
    const baseColor = isWhite ? themeColors.lightPiece : themeColors.darkPiece;
    const outlineColor = isWhite
      ? this.darken(baseColor, 0.45)
      : this.lighten(baseColor, 0.35);
    const highlightColor = isWhite
      ? this.lighten(baseColor, 0.25)
      : this.lighten(baseColor, 0.15);
    const shadowColor = isWhite
      ? this.darken(baseColor, 0.25)
      : this.darken(baseColor, 0.15);

    ctx.clearRect(0, 0, size, size);

    // Draw pixel by pixel with integer coords only
    for (let py = 0; py < 32; py++) {
      for (let px = 0; px < 32; px++) {
        const ch = pixels[py][px];
        if (ch === '.') continue;

        let c;
        if (ch === '#') c = baseColor;
        else if (ch === '*') c = highlightColor;
        else if (ch === '@') c = outlineColor;
        else c = baseColor;

        // Strict integer pixel positioning
        const drawX = Math.floor(px * pixelSize);
        const drawY = Math.floor(py * pixelSize);
        const drawW = Math.ceil(pixelSize);
        const drawH = Math.ceil(pixelSize);

        // Draw outline/shadow pixel slightly larger
        if (ch === '@') {
          ctx.fillStyle = outlineColor;
          ctx.fillRect(drawX - 1, drawY - 1, drawW + 2, drawH + 2);
        }

        ctx.fillStyle = c;
        ctx.fillRect(drawX, drawY, drawW, drawH);
      }
    }

    return canvas;
  }

  static lighten(hex, amt) {
    let r = parseInt(hex.slice(1, 3), 16);
    let g = parseInt(hex.slice(3, 5), 16);
    let b = parseInt(hex.slice(5, 7), 16);
    r = Math.min(255, Math.floor(r + amt * 255));
    g = Math.min(255, Math.floor(g + amt * 255));
    b = Math.min(255, Math.floor(b + amt * 255));
    const rs = r.toString(16).padStart(2, '0');
    const gs = g.toString(16).padStart(2, '0');
    const bs = b.toString(16).padStart(2, '0');
    return `#${rs}${gs}${bs}`;
  }

  static darken(hex, amt) {
    let r = parseInt(hex.slice(1, 3), 16);
    let g = parseInt(hex.slice(3, 5), 16);
    let b = parseInt(hex.slice(5, 7), 16);
    r = Math.max(0, Math.floor(r - amt * 255));
    g = Math.max(0, Math.floor(g - amt * 255));
    b = Math.max(0, Math.floor(b - amt * 255));
    const rs = r.toString(16).padStart(2, '0');
    const gs = g.toString(16).padStart(2, '0');
    const bs = b.toString(16).padStart(2, '0');
    return `#${rs}${gs}${bs}`;
  }

  static generateCharacterSprite(colors, size) {
    const canvas = document.createElement('canvas');
    canvas.width = size;
    canvas.height = size;
    const ctx = canvas.getContext('2d');
    ctx.imageSmoothingEnabled = false;

    const s = size;
    const cx = Math.floor(s / 2);
    const primary = colors.primary || '#ff4444';
    const secondary = colors.secondary || '#ff8888';
    const skin = colors.skin || '#ffcc99';
    const eye = colors.eye || '#ffffff';
    const pupil = colors.pupil || '#000000';

    ctx.clearRect(0, 0, s, s);

    // Body (pixel style)
    ctx.fillStyle = primary;
    ctx.fillRect(cx - Math.floor(s * 0.3), Math.floor(s * 0.5), Math.floor(s * 0.6), Math.floor(s * 0.4));
    ctx.fillRect(cx - Math.floor(s * 0.25), Math.floor(s * 0.4), Math.floor(s * 0.5), Math.floor(s * 0.15));

    // Head
    ctx.fillStyle = skin;
    ctx.fillRect(cx - Math.floor(s * 0.2), Math.floor(s * 0.1), Math.floor(s * 0.4), Math.floor(s * 0.35));
    ctx.fillRect(cx - Math.floor(s * 0.15), Math.floor(s * 0.05), Math.floor(s * 0.3), Math.floor(s * 0.1));

    // Hair
    ctx.fillStyle = secondary;
    ctx.fillRect(cx - Math.floor(s * 0.15), Math.floor(s * 0.05), Math.floor(s * 0.3), Math.floor(s * 0.08));

    // Eyes
    ctx.fillStyle = eye;
    ctx.fillRect(cx - Math.floor(s * 0.12), Math.floor(s * 0.18), Math.floor(s * 0.08), Math.floor(s * 0.06));
    ctx.fillRect(cx + Math.floor(s * 0.04), Math.floor(s * 0.18), Math.floor(s * 0.08), Math.floor(s * 0.06));
    ctx.fillStyle = pupil;
    ctx.fillRect(cx - Math.floor(s * 0.08), Math.floor(s * 0.18), Math.floor(s * 0.04), Math.floor(s * 0.04));
    ctx.fillRect(cx + Math.floor(s * 0.08), Math.floor(s * 0.18), Math.floor(s * 0.04), Math.floor(s * 0.04));

    // Mouth
    ctx.fillStyle = '#cc6666';
    ctx.fillRect(cx - Math.floor(s * 0.06), Math.floor(s * 0.28), Math.floor(s * 0.12), Math.floor(s * 0.03));

    return canvas;
  }
